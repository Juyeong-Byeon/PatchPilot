export function executorModeLabel(
  normalized: "single-pass" | "staged" | "other",
  raw: string,
  copy: AdminCopy,
): string {
  switch (normalized) {
    case "single-pass":
      return copy.executorModeSingle;
    case "staged":
      return copy.executorModeStaged;
    default:
      return raw;
  }
}

